    int red;
    int green;
    int blue;
    int code;
    String codename;

    Color(String colorCode, int code) {
        if (colorCode.length() != 6) {
            red = 0;
            green = 0;
            blue = 0;
            this.code = 0;
            codename = "000000";
        } else {
            try {
                red = Integer.parseInt(colorCode.substring(0, 1), 16);
                green = Integer.parseInt(colorCode.substring(2, 3), 16);
                blue = Integer.parseInt(colorCode.substring(4, 5), 16);
                codename = colorCode;
                this.code = code;
            } catch (Exception e) {
                red = 0;
                green = 0;
                blue = 0;
                this.code = 0;
                codename = "000000";
            }
        }
    }

    public static Color getFromCode(int code) {
        for (Color c: Color.values()) {
            if (c.code == code) {
                return c;
            }
        }
        return Color.Black;
    }
